main() {
    trap 'quit' INT
    check_screen_size
    use_colors=$(should_show_colors "$@")
    save_tty_settings
    frame_height=64
    if [ $LINES -ge $frame_height ]; then
        min_row=1
        max_row=$frame_height
    else
        min_row=$(((frame_height - (LINES-1)) / 2))
        max_row=$(((frame_height + (LINES-1)) / 2))
    fi
    printf '\033[H\033J' #clear
    i=0
    while true; do
        printf '\033[1;1H' #cursor position 1,1
        frame $i $min_row $max_row $use_colors
        delay 1
        i=$(((i + 1) % 12))
    done
    quit
}

frame() {
    min_row=$2 max_row=$3 use_colors=$4
    frame$1 | awk "NR >= $min_row && NR < $max_row" \
        | if $use_colors; then
            colorize
        else
            cat
        fi | ccut $COLUMNS
}

colorize() {
    awk -v esc='\033' -v apos="'" '{
        gsub(".", "&  ")
        # Blue background
        gsub(",", esc "[104m")
        # White stars
        gsub("\\.", esc "[107m")
        # Black border
        gsub(apos, esc "[40m")
        # Tan poptart
        gsub("@", esc "\[47m")
        # Pink poptart
        gsub("S", esc "[105m")
        # Red poptart
        gsub("-", esc "[101m")
        # Red rainbow
        gsub(">", esc "[101m")
        # Orange rainbow
        gsub("&", esc "[43m")
        # Yellow Rainbow
        gsub("\\+", esc "[103m")
        # Green rainbow
        gsub("""#", esc "[102m")
        # Light blue rainbow
        gsub("=", esc "[104m")
        # Dark blue rainbow
        gsub("\\"";", esc "[44m")
        # Gray cat face
        gsub("\\*", esc "[100m")
        # Pink cheeks
        gsub("%", esc "[105m")
        print
    }'
}

ccut() {
    awk -v max="$1" -v esc='\033' '#
        # Simulates `cut -c 1-X` for text containing ANSI color codes
        # Richard H. Tingstad  https://github.com/tingstad/dotfiles
        BEGIN {
            pattern = esc "\\[[0-9;]*[A-Za-z]"
            reset = esc "[0m"
        }
        {
        str = $0
        rest = $0
        len = 0
        stripped = 0
        while (1) {
            match(rest, pattern)
            if (RLENGTH == -1 || len + RSTART > max) {
                suffix = (stripped > 0 ? reset : "")
                print substr(str, 1, max + stripped) suffix
                break
            }
            else {
                stripped += RLENGTH
                len += RSTART - 1
                rest = substr(rest, RSTART + RLENGTH)
            }
        }
    }'
}

quit() {
    restore_tty_settings
    exit "${1:-0}"
}

should_show_colors() {
    tput_cmd=$(command -v tput)
    if [ -n "$tput_cmd" ]; then
        "$tput_cmd" setaf 1 >/dev/null 2>&1 \
        && printf 'true' \
        || printf 'false'
    else
        printf 'true' #default
    fi
}

check_screen_size() {
    if [ -z "$COLUMNS" ]; then
        _size=""
        # stty may fail with 'stty: standard input' if we are too quick (docker)
        _try=0
        while [ $((_try += 1)) -lt 9 ]; do
            _s="$(stty size 2>/dev/null)"
            case "$_s" in
                *[!0-9\ ]*) ;;  # Only digits and space
                *\ *\ *) ;;     # Just one space
                [0-9]*\ [0-9]*) _size="$_s"; break ;;
            esac
            delay 1
        done
        if [ -z "$_size" ]; then
            _size=$(stty size)
        fi
        COLUMNS=${_size#* }
        LINES=${_size% *}
    fi
    if ! is_number "$COLUMNS"; then
        printf "Unable to detect window width %s\n" "$COLUMNS" >&2
        quit 1
    fi
}

save_tty_settings() {
    saved_tty_settings="$(stty -g)"
}

restore_tty_settings() {
    [ -n "$saved_tty_settings" ] && stty "$saved_tty_settings"
}

delay() {
    stty -icanon -echo min 0 time "$1"
    dd bs=1 count=1 2>/dev/null
}

is_number() {
    case "$1" in
        *[!0-9]*) false ;;
        [0-9]*) true ;;
        *) false ;;
    esac
}

